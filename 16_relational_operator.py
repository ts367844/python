# relational operator in python
n1 = int(input("Enter number 1:"))
n2 = int(input("Enter number 2:"))

res = n1 == n2 
print(f"{res} = {n1} == {n2}") 

res = n1 != n2 
print(f"{res} = {n1} != {n2}") 

res = n1 < n2 
print(f"{res} = {n1} < {n2}") 

res = n1 > n2 
print(f"{res} = {n1} > {n2}") 

res = n1 <= n2 
print(f"{res} = {n1} <= {n2}") 

res = n1 >= n2 
print(f"{res} = {n1} >= {n2}") 